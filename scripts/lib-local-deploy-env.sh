qa_hint_evm_log_parse_failed() {
    local logf=$1
    echo "" >&2
    echo "=== QA: Could not read DEPLOYED_BRIDGE from forge output ===" >&2
    echo "  The deploy may have succeeded but log format changed. Try:" >&2
    echo "  1) Find DEPLOYED_BRIDGE in the output above, then:" >&2
    echo "       export EVM_BRIDGE_ADDRESS=0x...   EVM_CHAIN_REGISTRY=0x..." >&2
    echo "       ./scripts/setup-bridge.sh" >&2
    echo "  2) Or inspect: packages/contracts-evm/broadcast/DeployLocal.s.sol/31337/run-latest.json" >&2
    echo "  3) Report this to maintainers with the tail of the log below." >&2
    if [ -n "$logf" ] && [ -f "$logf" ]; then
        echo "" >&2
        echo "--- tail of forge log ---" >&2
        tail -40 "$logf" >&2
    fi
    echo "" >&2
}
