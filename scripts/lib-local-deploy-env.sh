    echo "  Expected container name: ${name}" >&2
    echo "  1) Start services: make start   or   docker compose up -d localterra" >&2
    echo "  2) Check: docker ps   and   make status" >&2
    echo "" >&2
}

qa_hint_forge_evm_deploy_failed() {
    local logf=$1
    echo "" >&2
    echo "=== QA: forge script (EVM deploy) failed ===" >&2
    echo "  1) Anvil must be running on http://localhost:8545:" >&2
    echo "       make start   or   docker compose up -d anvil" >&2
    echo "  2) Verify: cast block-number --rpc-url http://localhost:8545" >&2
    echo "  3) Re-run: make deploy-evm" >&2
    echo "  4) If forge still fails, see the log above. Last lines of captured output:" >&2
    if [ -n "$logf" ] && [ -f "$logf" ]; then
        tail -25 "$logf" >&2
    fi
    echo "" >&2
}

